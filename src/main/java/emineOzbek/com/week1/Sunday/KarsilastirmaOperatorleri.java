package emineOzbek.com.week1.Sunday;

public class KarsilastirmaOperatorleri {
    public static void main(String[] args) {

        /**
         * Eşittir : a == b
         * Eşit Değildir : a != b
         * Büyüktür : a > b
         * Küçüktür : a < b
         * Büyük Eşittir : a >= b
         * Küçük Eşittir : a <= b
         */

        int a = 4;
        int b = 5;
        System.out.println(a == b);

        a = b;
        System.out.println(a == b);


    }
}
